package Assignment2;

public class FindOddEven {

	public static void main(String[] args) {
		
		int x = 69; 
		
		//Initial Value (x = 100) 
		//Test for Values (x = 17), (x = 19), (x = 6), (x = 202), (x = 500), and (x = 69) 
		
		if (x % 2 == 0) {
			System.out.println(x + " is a Even Number.");	
		}
		else {
			System.out.println(x + " is a Odd Number.");
			
		}
			
	}

}
